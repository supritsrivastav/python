#ESCAPE SEQUENCE CHARACTER-To insert characters that are illegal in a string, use an escape character.
#An escape character is a backslash \ followed by the character you want to insert.
#\'	Single Quote	txt ='It\'s alright.'  ->It's alright.
#\\	Backslash	    txt = "This will insert one \\ (backslash)."  
# ->This will insert one \ (backslash).
#\n	New Line	    txt = "Hello\nWorld!"        
# ->Hello {World next line me print hoga}  World!
#\r	Carriage Return	 txt = "Hello\rWorld!"          ->Hello World!
#\t	Tab	            txt = "Hello\tWorld!"           ->Hello   World!
#\b	Backspace       txt = "Hello \bWorld!"          ->HelloWorld!
print("Hey i am a \n good boy")
print("Hey i am a\' good boy")
print("Hey i am a\\ good boy")
print("Hey i am a \r good boy")
print("Hey i am a \b good boy")
